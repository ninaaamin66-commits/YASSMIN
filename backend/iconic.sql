    id SERIAL PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    price DECIMAL(10,2) NOT NULL,
    description TEXT,
    media TEXT,
    colors TEXT,
    sizes TEXT,
    category_id INT REFERENCES categories(id)
);

